        </template>
        <template v-slot:right>
          <router-link  class="header_login" :to="userInfo._id?'/userinfo':'/login'">
              <span class="header_login_text" v-if="!userInfo._id">                  
                  登录/注册
              </span>
              <span class="header_login_text" v-else>                  
                  <i class="iconfont icon-user"></i>
              </span>
